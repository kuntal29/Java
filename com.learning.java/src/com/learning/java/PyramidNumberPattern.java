package com.learning.java;

public class PyramidNumberPattern {

	public static void main(String[] args) {
		for (int i =1; i<=5; i++) {
			System.out.println(i);
			for (int j = 1; j <= i; j++) {
				System.out.println(" ");
			}
			for (int k = 1; k<=j; k++) {
				System.out.println(k);
			}
		}

	}////// Incomplete program

}
